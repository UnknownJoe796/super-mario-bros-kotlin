    override val areaDataHOffsets = intArrayOf(0x2c, 0x0a, 0x27, 0x00)

    // EnemyDataAddrs for worlds 1-9: 51 entries
    // Order: Castle(0-9), Ground(10-38), Underground(39-43), Water(44-50) — all referenced by label
    // Addresses are synthetic (not real FDS addresses) — only the array index matters for Kotlin.
    // We use sequential fake addresses starting from 0x1000 to keep overflow logic working.
    override val enemyDataAddresses: IntArray = run {
        var addr = 0x1000
        IntArray(enemyDataArrays_19.size) { i ->
            val a = addr
            addr += enemyDataArrays_19[i].size
            a
        }
    }

    override val enemyDataArrays: Array<ByteArray> get() = enemyDataArrays_19

    override val enemyDataWithOverflow: Map<Int, ByteArray> by lazy {
        buildEnemyDataWithOverflow(enemyDataAddresses, enemyDataArrays)
    }

    // AreaDataAddrs for worlds 1-9: 51 entries
    override val areaDataAddresses: IntArray = run {
        var addr = 0x5000
        IntArray(areaDataArrays_19.size) { i ->
            val a = addr
            addr += areaDataArrays_19[i].size
            a
        }
    }

    override val areaDataArrays: Array<ByteArray> get() = areaDataArrays_19

    // ---- Worlds A-D index tables (from sm2data4.asm) ----

    val worldAddrOffsets_AD = intArrayOf(
        0, 5, 10, 14, 18,  // Worlds A-D + padding
        0, 0, 0, 0,        // padding (9 entries to match structure)
    )

    val areaAddrOffsets_AD = intArrayOf(
        // World A: $20, $2c, $40, $21, $60
        0x20, 0x2c, 0x40, 0x21, 0x60,
        // World B: $22, $2c, $00, $23, $61
        0x22, 0x2c, 0x00, 0x23, 0x61,
        // World C: $24, $25, $26, $62
        0x24, 0x25, 0x26, 0x62,
        // World D: $27, $28, $29, $63
        0x27, 0x28, 0x29, 0x63,
    )

    val enemyAddrHOffsets_AD = intArrayOf(0x14, 0x04, 0x12, 0x00)
    val areaDataHOffsets_AD = intArrayOf(0x14, 0x04, 0x12, 0x00)

    /** Worlds A-D enemy data arrays (21 entries). */
    val enemyDataArrays_AD: Array<ByteArray> get() = smb2jEnemyDataArrays_AD
    /** Worlds A-D area data arrays (21 entries). */
    val areaDataArrays_AD: Array<ByteArray> get() = smb2jAreaDataArrays_AD

    val enemyDataAddresses_AD: IntArray = run {
        var addr = 0x2000
        IntArray(smb2jEnemyDataArrays_AD.size) { i ->
            val a = addr
            addr += smb2jEnemyDataArrays_AD[i].size
            a
        }
    }

    val areaDataAddresses_AD: IntArray = run {
        var addr = 0x6000
        IntArray(smb2jAreaDataArrays_AD.size) { i ->
            val a = addr
            addr += smb2jAreaDataArrays_AD[i].size
            a
        }
    }

    val enemyDataWithOverflow_AD: Map<Int, ByteArray> by lazy {
        buildEnemyDataWithOverflow(enemyDataAddresses_AD, smb2jEnemyDataArrays_AD)
    }

    /** Halfway page nybbles for worlds A-D (from sm2data4 AtoDHalfwayPages) */
    val halfwayPages_AD = byteArrayOfUnsigned(0x76, 0x50, 0x65, 0x50, 0x75, 0xb0, 0x00, 0x00)

    /** Loopback offsets for castle loop detection (from sm2main AreaDataOfsLoopback) */
    val areaDataOfsLoopback = byteArrayOfUnsigned(
        0x0c, 0x0c, 0x42, 0x42, 0x10, 0x10, 0x30, 0x30, 0x06, 0x0c, 0x54, 0x06
    )
}

