/**
 * ROM data for Super Mario Bros. 2 (Japan, FDS version).
 *
 * SMB2J uses two independent sets of lookup tables:
 * - Worlds 1-9: 51 enemy/area entries, 9 world offsets (sm2main.asm)
 * - Worlds A-D: 21 enemy/area entries, 4 world offsets (sm2data4.asm)
 *
 * In the original FDS, these overlaid each other in memory. In Kotlin,
 * we keep them as separate lookup sets and switch based on hardWorldFlag.
 */
object Smb2jRomData : RomData {

    // ---- Worlds 1-9 index tables (from sm2main.asm) ----

    override val worldAddrOffsets = intArrayOf(
        0, 5, 9, 14, 18, 23, 28, 32, 36, // Worlds 1-9
