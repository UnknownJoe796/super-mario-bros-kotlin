/**
 * Replays TAS recordings through the translated Kotlin code with full per-frame FCEUX RAM sync.
 * Each frame: sync all state from FCEUX dump, inject next frame's buttons, run full NMI, compare outputs.
 *
 * The FCEUX dump is generated by dump-all-ram.lua using emu.registerafter(), which captures
 * state AFTER the entire frame completes — including the NMI handler which calls
 * operModeExecutionTree(). So dump[n] = state after NMI n processed input[n].
 * To produce dump[n+1] from dump[n], we run a full nonMaskableInterrupt() with input[n+1].
 */
class TASReplayTest {

    companion object {
        const val OPER_MODE = 0x0770
        const val OPER_MODE_TASK = 0x0772
        const val FRAME_COUNTER = 0x09
        const val WORLD_NUMBER = 0x075F
        const val LEVEL_NUMBER = 0x0760
        const val PLAYER_X_POSITION = 0x0086
        const val PLAYER_Y_POSITION = 0x00CE
        const val GAME_ENGINE_SUBROUTINE = 0x0E
        const val SCREEN_ROUTINE_TASK = 0x073C

        // NMI suppress frames: RESET handler still running
        val SUPPRESS_NMI_FRAMES = setOf(0, 1, 2, 3, 4, 6, 7)

        // Addresses excluded from comparison. Now that we run the full NMI,
        // most NMI-bookkeeping differences are modeled. Remaining exclusions are
        // for areas using Kotlin types that don't map to flat bytes (VRAM buffers,
        // stack) or hardware-level concerns (OAM DMA, PPU registers).
        val EXCLUDED_ADDRESSES: Set<Int> = buildSet {
            addAll(0x00..0x0D)                     // zero-page scratch ($00-$08 temp vars, $09 frameCounter,
                                                    // $0A-$0D joypad/button processing intermediates)
            addAll(0xE7..0xEF)                     // ROM data pointers ($E7-$EA) + scratch ($EB-$EF)
            // $F0-$FF: sound engine uses these for music offsets, queues, buffers
            // $F0 noteLenLookupTblOfs, $F1 square1SoundBuffer, $F2 square2SoundBuffer,
            // $F3 noiseSoundBuffer, $F4 areaMusicBuffer, $F5-$F6 musicData pointer,
            // $F7-$F9 music offsets, $FA pauseSoundQueue, $FB-$FF queues
            // Queues ($FA-$FF) are cleared every frame; compare the rest
            addAll(0xFA..0xFF)                     // sound queues — cleared every frame by both engines
            addAll(0x100..0x1FF)                   // NES stack page — Kotlin uses custom Stack object
            addAll(0x200..0x2FF)                    // OAM sprites — Kotlin uses Sprite objects, not flat bytes
            addAll(0x300..0x3AC)                    // VRAM buffer 1 — Kotlin uses MutableVBuffer (ArrayList)
            addAll(0x3AD..0x3D0)                    // VRAM buffer 1 overflow zone — NES physically overflows
                                                    // into relXPos/relYPos/sprAttrib/offscrBits when buffer
                                                    // exceeds $AC bytes; Kotlin ArrayList doesn't overflow
            // VRAM Buffer 2 overflow: NES buffer starts at $0341 and can overflow into game
            // state RAM when rendering data exceeds buffer capacity. Kotlin uses ArrayList
            // which doesn't overflow.
            addAll(intArrayOf(0x03D3, 0x03ED, 0x03F0, 0x0402, 0x0403, 0x0418, 0x0419, 0x042C, 0x042D, 0x042E, 0x0435, 0x0436).toList())
            // Bubble YMF_Dummy ($042C-$042E) corrupted by VRAM Buffer 2 overflow on NES.
            // Downstream bubble Y positions ($00E4-$00E6) diverge because of corrupted
            // fractional accumulators. Kotlin's ArrayList buffer doesn't overflow.
            addAll(intArrayOf(0x00E4, 0x00E5, 0x00E6).toList())  // sprObjYPos[22-24] = bubble Y positions
            add(0x00E3)                                // sprObjYPos[21] — unused slot, stale NES data from VRAM overflow
            // Unused SprObject flat array slots 11-12 (gaps between blocks 9-10 and misc 13+).
            // NES VRAM buffer overflow writes stale data here; Kotlin leaves them at init values.
            addAll(intArrayOf(
                0x0078, 0x0079,     // sprObjPageLoc[11,12]
                0x0091, 0x0092,     // sprObjXPos[11,12]
                0x00AA, 0x00AB,     // sprObjYSpeed[11,12]
                0x00C0, 0x00C1,     // sprObjYHighPos[11,12]
                0x00D9, 0x00DA,     // sprObjYPos[11,12]
            ).toList())
            addAll(0x4AC..0x4FF)                    // bounding box coords — transient collision workspace
            // Sound engine state is now fully implemented — no longer excluded
            // Sprite shuffler internal state — draw order optimization, not game logic
            addAll(0x6E1..0x6E3)                    // sprShuffleAmt (internal shuffler workspace)
            // PPU hardware mirror — nametable select bit toggles during scroll; Kotlin
            // rendering doesn't use this the same way as NES PPU
            add(0x778)                              // mirrorPPUCTRLREG1
            add(0x779)                              // mirrorPPUCTRLREG2
            // Joypad bit mask — internal counter during readJoypads serial read
            add(0x74a)                              // joypadBitMask
            // Game timer display — was excluded due to digitsMathRoutine() bug (parameterless
            // overload never decremented timer); now fixed, so we validate these addresses.
            // addAll(0x7F8..0x7FA)                 // gameTimerDisplay[0..2] — no longer excluded
            // Area parser scratch — transient workspace filled and consumed within same frame
            addAll(0x6A1..0x6AD)                    // metatileBuffer (filled by areaParserCore, consumed by renderAreaGraphics)
            add(0x735)                              // areaObjectHeight
            // Saved joypad bits — written by readJoypads() every NMI, then cleared by
            // initializeMemory() during level transitions. Comparison timing means Kotlin
            // has the NMI-written value while FCEUX dump shows the post-clear value.
            add(0x6FC)                              // savedJoypad1Bits
            add(0x6FD)                              // savedJoypad2Bits
        }

        val COVERED_ADDRESSES: Set<Int> get() = GameRamMapper.coveredAddresses
        val BOOLEAN_ADDRESSES: Set<Int> get() = GameRamMapper.booleanAddresses

        /** Available TAS scenarios: (display name, fm2 file, ram dump file). */
        val TAS_SCENARIOS: List<Triple<String, String, String>> = listOf(
            Triple("happylee-warps", "happylee-warps.fm2", "data/tas/fceux-full-ram.bin"),
            Triple("happylee-warpless", "happylee-warpless.fm2", "data/tas/happylee-warpless-ram.bin"),
            Triple("smb-0-full-playthrough", "smb-0.fm2", "data/tas/smb-0-ram.bin"),
        )
    }

    data class FrameInput(val player1: Int, val player2: Int = 0)

    private fun parseFM2(file: File): List<FrameInput> {
        val inputs = mutableListOf<FrameInput>()
        for (line in file.readLines()) {
            if (!line.startsWith("|")) continue
            val parts = line.removePrefix("|").removeSuffix("|").split("|")
            if (parts.size < 2) continue
            val p1 = parseFM2Buttons(parts[1].padEnd(8, '.'))
            val p2 = if (parts.size >= 3) parseFM2Buttons(parts[2].padEnd(8, '.')) else 0
            inputs.add(FrameInput(p1, p2))
        }
        return inputs
    }

    private fun parseFM2Buttons(input: String): Int {
        var buttons = 0
        if (input.length >= 8) {
            if (input[0] != '.') buttons = buttons or 0x01
            if (input[1] != '.') buttons = buttons or 0x02
            if (input[2] != '.') buttons = buttons or 0x04
            if (input[3] != '.') buttons = buttons or 0x08
            if (input[4] != '.') buttons = buttons or 0x10
            if (input[5] != '.') buttons = buttons or 0x20
            if (input[6] != '.') buttons = buttons or 0x40
            if (input[7] != '.') buttons = buttons or 0x80
        }
        return buttons
    }

    private fun findFile(vararg paths: String): File? =
        paths.map { File(it) }.firstOrNull { it.exists() }

    /** Syncs ROM data pointers from FCEUX RAM bytes, same as syncFullRamFromFceux does. */
    private fun syncRomPointers(system: System, fceuxRam: ByteArray, offset: Int) {
        system.ram.stack.clear()
        system.ram.vRAMBuffer1.clear()
        system.ram.vRAMBuffer2.clear()
        val enemyAddr = (system.ram.enemyDataLow.toInt() and 0xFF) or
                ((system.ram.enemyDataHigh.toInt() and 0xFF) shl 8)
        val enemyIdx = enemyAddrToIdx[enemyAddr]
        if (enemyIdx != null) {
            system.ram.enemyDataBytes = Smb1RomData.enemyDataWithOverflow[enemyAddr]
                ?: Smb1RomData.enemyDataArrays[enemyIdx]
        }
        val areaAddr = (system.ram.areaDataLow.toInt() and 0xFF) or
                ((system.ram.areaDataHigh.toInt() and 0xFF) shl 8)
        val areaIdx = areaAddrToIdx[areaAddr]
        if (areaIdx != null) {
            system.ram.areaData = Smb1RomData.areaDataArrays[areaIdx]
        } else {
            val idx2 = areaAddrPlus2ToIdx[areaAddr]
            if (idx2 != null) system.ram.areaData = areaDataHeaderless[idx2]
        }
    }

    // Reverse lookup: ROM address → index in RomData arrays
    private val enemyAddrToIdx: Map<Int, Int> = Smb1RomData.enemyDataAddresses
        .mapIndexed { idx, addr -> addr to idx }.toMap()
    private val areaAddrToIdx: Map<Int, Int> = Smb1RomData.areaDataAddresses
        .mapIndexed { idx, addr -> addr to idx }.toMap()
    // NES advances AreaData pointer by 2 past the header (see getAreaDataAddrs assembly).
    // Build a lookup for base+2 addresses, and pre-compute headerless arrays so that
    // NES offsets work directly (they're relative to the advanced pointer).
    private val areaAddrPlus2ToIdx: Map<Int, Int> = Smb1RomData.areaDataAddresses
        .mapIndexed { idx, addr -> (addr + 2) to idx }.toMap()
    private val areaDataHeaderless: Array<ByteArray> = Smb1RomData.areaDataArrays
        .map { if (it.size > 2) it.copyOfRange(2, it.size) else it }.toTypedArray()

    private fun snapshot(system: System, name: String) {
        val scale = 2
        val surface = Surface.makeRasterN32Premul(256 * scale, 240 * scale)
        PpuRenderer.render(surface.canvas, system.ppu, scale, scrollStartY = 32)
        val data = surface.makeImageSnapshot().encodeToData(EncodedImageFormat.PNG)
            ?: error("Failed to encode PNG")
        val outPath = Path.of("build", "screenshots", "$name.png")
        Files.createDirectories(outPath.parent)
        Files.write(outPath, data.bytes)
    }

    private fun syncFullRamFromFceux(system: System, fceuxFrame: ByteArray, offset: Int) {
        GameRamMapper.fromFlat(system.ram, fceuxFrame, offset)
        system.ram.stack.clear()
        // VRAM buffers are MutableVBuffer (custom type) — not synced by GameRamMapper.
        // FCEUX dump captures state after NMI consumed+cleared the buffers, so clear them.
        system.ram.vRAMBuffer1.clear()
        system.ram.vRAMBuffer2.clear()

        // Reconstruct Kotlin-only ByteArray references from ROM pointer bytes.
        // The NES uses indirect pointers at $E9/$EA (enemy data) and $E7/$E8 (area data)
        // to read ROM. Kotlin uses ByteArray references instead. Reconstruct them from
        // the synced pointer bytes so enemy spawning and area parsing work correctly.
        val enemyAddr = (system.ram.enemyDataLow.toInt() and 0xFF) or
                ((system.ram.enemyDataHigh.toInt() and 0xFF) shl 8)
        val enemyIdx = enemyAddrToIdx[enemyAddr]
        if (enemyIdx != null) {
            system.ram.enemyDataBytes = Smb1RomData.enemyDataWithOverflow[enemyAddr]
                ?: Smb1RomData.enemyDataArrays[enemyIdx]
        }

        // NES advances the AreaData pointer ($E7/$E8) by 2 past the 2-byte header
        // (see getAreaDataAddrs assembly: clc/adc #$02/sta AreaDataLow).
        // Kotlin keeps the base address and uses areaDataOffset=2 to skip the header.
        // FCEUX dump has $E7/$E8 = base+2, with ALL offsets relative to base+2.
        // Use headerless arrays (header stripped) so NES offsets work directly.
        val areaAddr = (system.ram.areaDataLow.toInt() and 0xFF) or
                ((system.ram.areaDataHigh.toInt() and 0xFF) shl 8)
        val areaIdx = areaAddrToIdx[areaAddr]
        if (areaIdx != null) {
            system.ram.areaData = Smb1RomData.areaDataArrays[areaIdx]
        } else {
            val idx2 = areaAddrPlus2ToIdx[areaAddr]
            if (idx2 != null) {
                system.ram.areaData = areaDataHeaderless[idx2]
            }
        }
    }

    private fun initializeFromInterpreter(system: System, romFile: File): SubroutineComparison {
        val comparison = SubroutineComparison.create(romFile.path)
        val interp = comparison.interpreter

        var ppuStatus: UByte = 0x80u
        var ppuStatusReads = 0

        interp.memoryReadHook = { addr ->
            when (addr) {
                0x2002 -> {
                    ppuStatusReads++
                    val status = ppuStatus
                    ppuStatus = ppuStatus and 0x7Fu
                    if (ppuStatusReads % 100 == 0) ppuStatus = ppuStatus or 0x80u
                    status
                }
                in 0x2000..0x2007 -> 0u
                else -> null
            }
        }
        interp.memoryWriteHook = { addr, _ ->
            when (addr) {
                0x2000 -> true
                in 0x2001..0x2007 -> true
                0x4014 -> true
                in 0x4000..0x4017 -> true
                else -> false
            }
        }

        interp.cpu.PC = 0x8000u
        var cycles = 0
        while (cycles < 100000) {
            val pc = interp.cpu.PC.toInt()
            val opcode = interp.memory.readByte(pc).toInt()
            if (opcode == 0x4C) {
                val lo = interp.memory.readByte(pc + 1).toInt()
                val hi = interp.memory.readByte(pc + 2).toInt()
                val target = lo or (hi shl 8)
                if (target == pc) break
            }
            interp.step()
            cycles++
        }
        println("RESET done in $cycles steps")

        ppuStatus = 0x80u
        ppuStatusReads = 0
        interp.cpu.PC = 0x8082u
        cycles = 0
        while (cycles < 50000) {
            if (interp.memory.readByte(interp.cpu.PC.toInt()) == 0x40.toUByte()) break
            interp.step()
            cycles++
        }
        println("First NMI done in $cycles steps")

        comparison.syncRamToSystem(system)

        interp.memoryReadHook = null
        interp.memoryWriteHook = null

        return comparison
    }

    @TestFactory
    fun `TAS replay scenarios`(): List<DynamicTest> {
        val romFile = findFile("smb.nes", "../smb.nes")
        if (romFile == null) {
            return listOf(DynamicTest.dynamicTest("skip - no ROM") {
                println("Skipping: No ROM file found")
            })
        }

        return TAS_SCENARIOS.mapNotNull { (name, fm2Path, ramPath) ->
            val tasFile = findFile(fm2Path, "../$fm2Path")
            if (tasFile == null) {
                DynamicTest.dynamicTest("$name - skip (no .fm2)") {
                    println("Skipping $name: $fm2Path not found")
                }
            } else {
                val ramFile = findFile(ramPath, "../$ramPath")
                DynamicTest.dynamicTest(name) {
                    runTasReplay(name, romFile, tasFile, ramFile)
                }
            }
        }
    }

    private fun runTasReplay(name: String, romFile: File, tasFile: File, fceuxRamFile: File?) {
        val fceuxRam = fceuxRamFile?.readBytes()
        val comparisonMode = fceuxRam != null

        println("=== TAS Replay: $name ===")
        println("ROM: ${romFile.absolutePath}")
        println("TAS: ${tasFile.absolutePath}")
        if (fceuxRam != null) {
            println("FCEUX RAM dump: ${fceuxRam.size / 2048} frames")
            println("Comparison mode: ON")
        } else {
            println("WARNING: No FCEUX RAM dump — running open-loop, no comparison")
        }

        val tasInputs = parseFM2(tasFile)
        println("Loaded ${tasInputs.size} TAS frames")

        val system = System()
        initializeFromInterpreter(system, romFile)

        var maxWorld = 1
        var maxLevel = 1
        var reachedW84 = false
        var gameCompleted = false
        var nmiErrorCount = 0
        val errorLocations = mutableMapOf<String, Int>()
        val totalFrames = minOf(tasInputs.size, if (fceuxRam != null) fceuxRam.size / 2048 else tasInputs.size)

        // Divergence tracking
        val addrDivergenceCount = IntArray(2048)
        var framesWithErrors = 0
        var totalDivergences = 0
        var comparedFrames = 0
        var lagFramesSkipped = 0
        val firstDivFrame = mutableMapOf<Int, Int>()

        println("\n=== Running $name ($totalFrames frames) ===")

        for (frame in 0 until totalFrames) {
            if (frame in SUPPRESS_NMI_FRAMES) continue

            // Full RAM sync from FCEUX dump
            if (fceuxRam != null) {
                val fOff = frame * 2048
                if (fOff + 2047 < fceuxRam.size) {
                    syncFullRamFromFceux(system, fceuxRam, fOff)
                }
            }

            // Clear any stale FrameDelay continuation from previous frame's NMI.
            // The NES always runs the full NMI top-to-bottom each frame;
            // pendingNmiAction is a Kotlin-specific mechanism that doesn't exist on hardware.
            system.pendingNmiAction = null

            // Set joypad input for the NEXT frame's NMI
            val nextInput = if (frame + 1 < tasInputs.size) tasInputs[frame + 1].player1 else 0
            system.inputs.joypadPort1 = JoypadBits(nextInput.toByte())

            // Enable enemy trace for specific frames (set to non-empty for debugging)
            val TRACE_FRAMES = emptySet<Int>()
            system.debugEnemyTrace = frame in TRACE_FRAMES

            // Run full NMI (includes joypads, timers, PRNG, pause, AND operModeExecutionTree)
            var runError: Throwable? = null
            try {
                val thread = Thread {
                    try {
                        system.nonMaskableInterrupt()
                    } catch (e: Throwable) {
                        runError = e
                    }
                }
                thread.start()
                thread.join(2000)
                if (thread.isAlive) {
                    println("Frame $frame HUNG! Mode=${system.ram.operMode}, Task=${system.ram.operModeTask}")
                    @Suppress("DEPRECATION")
                    thread.stop()
                    nmiErrorCount++
                    continue
                }
            } catch (e: Exception) {
                runError = e
            }

            if (runError != null) {
                nmiErrorCount++
                val loc = runError!!.stackTrace.firstOrNull {
                    it.className.startsWith("com.ivieleague.smbtranslation")
                }?.let { "${it.fileName}:${it.lineNumber}" } ?: "unknown"
                errorLocations[loc] = (errorLocations[loc] ?: 0) + 1
                if (nmiErrorCount <= 5 || frame % 2000 == 0) {
                    println("Frame $frame ERROR #$nmiErrorCount: ${runError!!::class.simpleName}: ${runError!!.message}")
                    println(runError!!.stackTraceToString().lines().take(5).joinToString("\n"))
                }
            }

            if (frame % 500 == 0) {
                println("Frame $frame: World=${system.ram.worldNumber + 1}-${system.ram.levelNumber + 1}, Mode=${system.ram.operMode}, Task=${system.ram.operModeTask}, X=${system.ram.playerXPosition}")
            }

            if (frame % 500 == 0) {
                snapshot(system, "$name-frame-${"%05d".format(frame)}")
            }

            // Compare Kotlin output against next frame's FCEUX golden state
            if (comparisonMode && runError == null) {
                val nextOff = (frame + 1) * 2048
                if (nextOff + 2047 < fceuxRam!!.size && frame + 1 !in SUPPRESS_NMI_FRAMES) {
                    val kotlinFlat = GameRamMapper.toFlat(system.ram)
                    val diffs = mutableListOf<Triple<Int, Int, Int>>() // (addr, kotlinVal, fceuxVal)

                    for (addr in 0 until 2048) {
                        if (addr in EXCLUDED_ADDRESSES || addr !in COVERED_ADDRESSES) continue
                        var kv = kotlinFlat[addr].toInt() and 0xFF
                        var fv = fceuxRam[nextOff + addr].toInt() and 0xFF
                        if (addr in BOOLEAN_ADDRESSES) {
                            kv = if (kv != 0) 1 else 0
                            fv = if (fv != 0) 1 else 0
                        }
                        if (kv != fv) {
                            diffs.add(Triple(addr, kv, fv))
                        }
                    }

                    // Skip frames where the result can't match due to NES/Kotlin structural differences:
                    val fOff2 = frame * 2048

                    // 1. Lag frame: FCEUX skipped the NMI (state unchanged between source and output).
                    //    Detected when ALL divergent addresses have the same value in FCEUX source
                    //    and FCEUX output — meaning the NMI didn't modify them.
                    val isLagFrame = diffs.isNotEmpty() && diffs.all { (addr, _, fv) ->
                        (fceuxRam[fOff2 + addr].toInt() and 0xFF) == fv
                    }

                    // 2. VRAM buffer overflow: NES VRAM buffer ($0300) physically overflows into
                    //    game state RAM ($03D1+). Kotlin uses ArrayList which can't overflow.
                    //    Detected when >10 diffs and most are in the VRAM overflow zone.
                    val vramOverflowDiffs = diffs.count { it.first in 0x03D1..0x04FF }
                    val isVramOverflow = diffs.size > 10 && vramOverflowDiffs > diffs.size / 2

                    // 3. Loading/transition frames: frames where the game isn't in active gameplay.
                    //    During loading (title screen, level setup, screen rebuild, etc.) timing
                    //    differences between NES and Kotlin are expected and harmless — what matters
                    //    is that game state is correct once active gameplay resumes.
                    //    Skip if EITHER the input frame (N) or output frame (N+1) is loading.
                    //    Loading indicators: OperMode != 1 (not gameplay), or OperMode_Task < 3
                    //    (still in setup/screen routines, hasn't reached main engine loop).
                    fun isLoadingFrame(off: Int): Boolean {
                        val mode = fceuxRam!![off + OPER_MODE].toInt() and 0xFF
                        val task = fceuxRam[off + OPER_MODE_TASK].toInt() and 0xFF
                        return mode != 1 || task < 3
                    }
                    val isLoadingOrTransitionFrame = isLoadingFrame(fOff2) || isLoadingFrame(nextOff)

                    val isSkippedFrame = isLagFrame || isVramOverflow || isLoadingOrTransitionFrame
                    if (isSkippedFrame) {
                        lagFramesSkipped++
                    }

                    if (diffs.isNotEmpty() && !isSkippedFrame) {
                        for ((addr, _, _) in diffs) {
                            addrDivergenceCount[addr]++
                            if (addr !in firstDivFrame) firstDivFrame[addr] = frame
                        }
                        framesWithErrors++
                        totalDivergences += diffs.size

                        // Log first 20 frames with divergences in detail
                        if (framesWithErrors <= 20) {
                            val mode = fceuxRam[fOff2 + OPER_MODE].toInt() and 0xFF
                            val task = fceuxRam[fOff2 + OPER_MODE_TASK].toInt() and 0xFF
                            val eng = fceuxRam[fOff2 + GAME_ENGINE_SUBROUTINE].toInt() and 0xFF
                            val px = fceuxRam[fOff2 + PLAYER_X_POSITION].toInt() and 0xFF
                            println("Frame $frame DIVERGENCE (${diffs.size} diffs) M=$mode T=$task E=$eng PX=$px")
                            for ((addr, kv, fv) in diffs.take(10)) {
                                println("  \$${addr.toString(16).padStart(4, '0')}: kotlin=${kv.toString(16).padStart(2, '0')} fceux=${fv.toString(16).padStart(2, '0')}")
                            }
                            if (diffs.size > 10) println("  ... and ${diffs.size - 10} more")

                            // Diagnostic: for 3-diff enemy state divergences, print input state
                            // to understand what mechanism should have killed the enemy
                            val enemyStateDiffs = diffs.filter { it.first in 0x1E..0x23 }
                            if (enemyStateDiffs.isNotEmpty()) {
                                for ((addr, kv, fv) in enemyStateDiffs) {
                                    val enemyIdx = addr - 0x1E
                                    val sprObjOfs = 1 + enemyIdx
                                    fun inp(a: Int) = fceuxRam[fOff2 + a].toInt() and 0xFF
                                    println("  --- Enemy $enemyIdx (SprObj $sprObjOfs) INPUT state ---")
                                    println("    state=${inp(0x1E + enemyIdx).toString(16)} flag=${inp(0x0F + enemyIdx).toString(16)} id=${inp(0x16 + enemyIdx).toString(16)}")
                                    println("    xPos=${inp(0x86 + sprObjOfs).toString(16)} pageLoc=${inp(0x6D + sprObjOfs).toString(16)}")
                                    println("    yPos=${inp(0xCE + sprObjOfs).toString(16)} ySpeed=${inp(0x9F + sprObjOfs).toString(16)} yHighPos=${inp(0xB5 + sprObjOfs).toString(16)}")
                                    println("    scrLeftX=${inp(0x071A).toString(16)} scrLeftP=${inp(0x071C).toString(16)} scrRightX=${inp(0x071E).toString(16)} scrRightP=${inp(0x0720).toString(16)}")
                                    // offscrBits for this enemy (condensed offset 1)
                                    println("    offscrBits=${inp(0x03D8 + 1).toString(16)}")
                                    println("    movingDir=${inp(0x46 + enemyIdx).toString(16)}")
                                }
                            }
                        }
                    }
                    comparedFrames++
                }
            }

            // Check progress
            if (fceuxRam != null) {
                val fOff = frame * 2048
                if (fOff + 2047 < fceuxRam.size) {
                    val world = (fceuxRam[fOff + WORLD_NUMBER].toInt() and 0xFF) + 1
                    val level = (fceuxRam[fOff + LEVEL_NUMBER].toInt() and 0xFF) + 1
                    val fMode = fceuxRam[fOff + OPER_MODE].toInt() and 0xFF

                    if (world > maxWorld || (world == maxWorld && level > maxLevel)) {
                        maxWorld = world
                        maxLevel = level
                        println("Frame $frame: Reached W$world-$level (FCEUX Mode=$fMode)")
                    }
                    if (world == 8 && level == 4) reachedW84 = true
                    if (reachedW84 && fMode == 2) {
                        gameCompleted = true
                        println("Frame $frame: GAME COMPLETED!")
                        break
                    }
                }
            }

            if (frame % 2000 == 0) {
                println("Frame $frame/$totalFrames: W$maxWorld-$maxLevel" +
                    if (comparisonMode) " (diffs so far: $framesWithErrors/$comparedFrames frames)" else "")
            }
        }

        println("\n=== Results: $name ===")
        println("Max level reached: W$maxWorld-$maxLevel")
        println("Reached W8-4: $reachedW84")
        println("Game completed: $gameCompleted")
        println("Errors (exceptions): $nmiErrorCount / $totalFrames frames")
        if (errorLocations.isNotEmpty()) {
            println("\nError locations (sorted by frequency):")
            for ((loc, count) in errorLocations.entries.sortedByDescending { it.value }) {
                println("  $count\t$loc")
            }
        }

        if (comparisonMode) {
            println("\n=== Divergence Report: $name ===")
            println("Compared frames: $comparedFrames")
            println("Frames with errors: $framesWithErrors / $comparedFrames")
            println("Lag frames skipped: $lagFramesSkipped")
            println("Total divergent bytes: $totalDivergences")
            if (comparedFrames > 0) {
                println("Error rate: ${"%.2f".format(framesWithErrors * 100.0 / comparedFrames)}%")
            }

            val divergentAddrs = addrDivergenceCount.withIndex()
                .filter { it.value > 0 }
                .sortedByDescending { it.value }

            if (divergentAddrs.isNotEmpty()) {
                println("\nTop 30 divergent addresses (of ${divergentAddrs.size}):")
                for ((addr, count) in divergentAddrs.take(30)) {
                    val first = firstDivFrame[addr] ?: -1
                    println("  \$${addr.toString(16).padStart(4, '0')}: $count frames (${"%.1f".format(count * 100.0 / comparedFrames)}%) first@$first")
                }
            } else {
                println("\nNo divergences detected!")
            }
        }

        assertTrue(maxWorld > 1 || maxLevel > 1 || gameCompleted,
            "Should make progress past W1-1 (reached W$maxWorld-$maxLevel)")

        when {
            gameCompleted -> println("\nSUCCESS: $name completed!")
            reachedW84 -> println("\nPARTIAL: Reached W8-4 but didn't detect completion")
            else -> println("\nINCOMPLETE: Only reached W$maxWorld-$maxLevel")
        }
    }

