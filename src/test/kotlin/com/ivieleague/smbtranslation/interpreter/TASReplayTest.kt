                    val iv = interpSnap.values[a]?.toInt()?.and(0xFF) ?: -1
                    val kv = ktFlat[a].toInt() and 0xFF
                    println("  \$${a.toString(16)}: interp=$iv kotlin=$kv")
                }
            }

            // Also check what FCEUX shows for the NEXT frame
            val nextOff = (frame + 1) * 2048
            if (nextOff + 2047 < fceuxRam.size) {
                println("FCEUX next frame:")
                for (a in interestAddrs) {
                    val fv = fceuxRam[nextOff + a].toInt() and 0xFF
                    println("  \$${a.toString(16)}: $fv")
                }
            }
        }
